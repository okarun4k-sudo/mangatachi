// Funções simplificadas para evitar conflitos
function downloadHasChapters(mangaId) {
    return mangaDownloads[mangaId] && Object.keys(mangaDownloads[mangaId].chapters).length > 0;
}

function downloadGetChapters(mangaId) {
    return mangaDownloads[mangaId] ? mangaDownloads[mangaId].chapters : {};
}

function downloadGetChapterLink(mangaId, chapterNumber) {
    const chapters = downloadGetChapters(mangaId);
    return chapters[chapterNumber] || null;
}

function downloadGetChaptersCount(mangaId) {
    const chapters = downloadGetChapters(mangaId);
    return Object.keys(chapters).length;
}

function downloadGetMangaInfo(mangaId) {
    return mangaDownloads[mangaId] || null;
}